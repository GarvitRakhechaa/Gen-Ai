# 5. Weather Activity Suggestion
# Problem: Suggest an activity based on the weather 
# (e.g., Sunny - Go for a walk, Rainy - Read a book, Snowy - Build a snowman).

Weather = "rainy"

if Weather == "sunny":
    print("Go for a walk")
elif Weather == "rainy":
    print("Read a book")
elif Weather == "snowy":
    print("Build a snowman")